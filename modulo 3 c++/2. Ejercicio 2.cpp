#include <iostream>
#include <cmath>
using namespace std;

int main() {
    float num1, num2;
    cin >> num1 >> num2;
    cout << num1 + num2 << endl;
    cout << num1 - num2 << endl;
    cout << num1 * num2 << endl;
    cout << num1 / num2 << endl;
    cout << pow(num1, num2) << endl;
    cout << sqrt(num1) << endl;
    return 0;
}